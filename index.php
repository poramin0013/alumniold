<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">&nbsp; &nbsp; &nbsp; &nbsp;สาขาวิชาวิศวกรรมซอฟต์แวร์ก่อตั้งขึ้นในปีพ.ศ. 2552 โดย อ.เสวี เหลือบุญชู เริ่มแรกนั้นใช้ชื่อว่าสาขาวิชาการพัฒนาซอฟต์แวร์</h3>
<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">คณะกรรมการบริหารสาขาวิชาซึ่งมี อ.เสวี เหลือบุญชู เป็นประธานสาขาคนแรก มีหน้าที่ดูแลบริหารหลักสูตร วิทยาศาสตรบัณฑิต</h3>
<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">สาขาการพัฒนาซอฟต์แวร์ หลักสูตรปี<span style="text-align: justify;">พ.ศ. 2551 ซึ่งเป็นหลักสูตรปริญญาตรี 4 ปี เริ่มรับนักศึกษารุ่นแรกในปี&nbsp;2552&nbsp;&nbsp;</span>หลังจากนั้น</h3>
<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">เมื่อกระทรวงศึกษาธิการได้ประกาศใช้กรอบมาตรฐานคุณวุฒิระดับอุดมศึกษาแห่งชาติ ซึ่งเป็นกรอบที่แสดงระบบคุณวุฒิการศึกษา</h3>
<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">ระดับอุดมศึกษาของประเทศ&nbsp; โดยในส่วนของมาตรฐานคุณวุฒิระดับปริญญาตรี สาขาคอมพิวเตอร์นั้นจะประกอบไปด้วย 5 สาขา</h3>
<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">ด้วยกันคือ สาขาวิทยาการคอมพิวเตอร์ สาขาวิศวกรรมคอมพิวเตอร์ สาขาวิศวกรรมซอฟต์แวร์ สาขาเทคโนโลยีสารสนเทศ และ</h3>
<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">สาขาคอมพิวเตอร์ธุรกิจ&nbsp; ดังนั้นคณะกรรมการบริหารสาขาจึงเห็นควรให้มีการปรับปรุงหลักสูตรเพื่อให้สอดคล้องกับกรอบมาตรฐาน</h3>
<h3 style="text-align: justify; font-family:TH Sarabun New; font-weight-bold New;  color:#2D191A">คุณวุฒิระดับอุดมศึกษาแห่งชาติ จึงได้พัฒนาหลักสูตร&nbsp;วิทยาศาสตรบัณฑิต สาขาวิศวกรรมซอฟต์แวร์ หลักสูตรปี พ.ศ. 2553 และ</h3>
